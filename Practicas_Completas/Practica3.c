// Función para imprimir la dirección MAC de destino (primeros 6 bytes)
void imprimir_mac_destino(const uint8_t trama[]) {
    printf("\nLa MAC destino es: ");
    for (unsigned int i = 0; i < 6; i++) {
        printf("%02x", trama[i]);
        if (i < 5) {
            printf(":");
        }
    }
    printf("\n");
}

// Función para imprimir la dirección MAC de origen (bytes 6 a 11)
void imprimir_mac_origen(const uint8_t trama[]) {
    printf("La MAC origen es: ");
    for (unsigned int i = 6; i < 12; i++) {
        printf("%02x", trama[i]);
        if (i < 11) {
            printf(":");
        }
    }
    printf("\n");
}

